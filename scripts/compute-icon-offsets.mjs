// One-off dev tool (not wired into `prebuild`, run manually when icons
// change) — measures the exact rendered geometry bounding box of every
// icon in ITEM_ICON_LIB via headless Chromium's native getBBox(), then
// rewrites the generated ICON_OFFSETS block in src/lib/itemIcons.js so
// each icon's translate recenters its own ink at viewBox center (50,50).
// Icons are hand-drawn and not necessarily symmetric within their 100x100
// viewBox (e.g. a container's neck/cap vs. body), so the badge circle's
// own centering (fixed by ItemGridCard.jsx/index.css) isn't enough — the
// artwork itself needs a per-icon correction.
// Requires the `playwright` package + a Chromium binary at
// /opt/pw-browsers/chromium (both pre-installed in Claude Code sessions on
// this project; not a project dependency — symlink it into node_modules
// locally if running elsewhere: `ln -s "$(npm root -g)/playwright" node_modules/playwright`).
import { chromium } from "playwright";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const iconsPath = path.join(root, "src", "lib", "itemIcons.js");

const START = "// ---- ICON_OFFSETS: generated by scripts/compute-icon-offsets.mjs, do not hand-edit ----";
const END = "// ---- end ICON_OFFSETS ----";

async function main() {
  const source = readFileSync(iconsPath, "utf8");
  const mod = await import(iconsPath);
  const lib = mod.ITEM_ICON_LIB;

  const browser = await chromium.launch({ executablePath: "/opt/pw-browsers/chromium" });
  const page = await browser.newPage();

  const offsets = {};
  const warnings = [];
  for (const key of Object.keys(lib)) {
    const inner = lib[key];
    if (!inner) continue;
    await page.setContent(
      `<svg viewBox="0 0 100 100" width="100" height="100" xmlns="http://www.w3.org/2000/svg"><g id="m">${inner}</g></svg>`
    );
    const bbox = await page.evaluate(() => {
      const el = document.getElementById("m");
      const b = el.getBBox();
      return { x: b.x, y: b.y, width: b.width, height: b.height };
    });
    const cx = bbox.x + bbox.width / 2;
    const cy = bbox.y + bbox.height / 2;
    const dx = Math.round((50 - cx) * 10) / 10;
    const dy = Math.round((50 - cy) * 10) / 10;
    if (dx || dy) offsets[key] = [dx, dy];

    const newMinX = bbox.x + dx;
    const newMaxX = bbox.x + bbox.width + dx;
    const newMinY = bbox.y + dy;
    const newMaxY = bbox.y + bbox.height + dy;
    if (newMinX < 0 || newMaxX > 100 || newMinY < 0 || newMaxY > 100) {
      warnings.push(`${key}: shifted bbox [${newMinX.toFixed(1)},${newMinY.toFixed(1)} .. ${newMaxX.toFixed(1)},${newMaxY.toFixed(1)}] extends outside 0-100, may clip`);
    }
  }

  await browser.close();

  const entries = Object.keys(offsets)
    .sort()
    .map((k) => `  ${JSON.stringify(k)}: [${offsets[k][0]}, ${offsets[k][1]}]`)
    .join(",\n");
  const block = `${START}\nvar ICON_OFFSETS = {\n${entries}\n};\n${END}`;

  const blockRe = new RegExp(`${START}[\\s\\S]*?${END}`);
  const updated = blockRe.test(source)
    ? source.replace(blockRe, block)
    : source.replace(/function iconSvg\(name\) \{/, `${block}\n\n  function iconSvg(name) {`);

  writeFileSync(iconsPath, updated);
  console.log(`Wrote ${Object.keys(offsets).length} icon offsets (of ${Object.keys(lib).length} total icons) to ${path.relative(root, iconsPath)}`);
  if (warnings.length) {
    console.log(`\n${warnings.length} icon(s) may clip after recentering — spot-check these:`);
    for (const w of warnings) console.log(`  - ${w}`);
  }
}

main();
